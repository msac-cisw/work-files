/*
What is a variable?

A variable is a container for a value, like a number we might use in a sum, or a string that we might use as part of a sentence.

* Variable names can include letters of the alphabet, both upper- and lowercase. They can also include the digits 0–9 and the underscore (_) character.
* Variable names cannot include spaces or any other punctuation characters.
* The first character of the variable name must be either a letter or an underscore.
* Variable names are case sensitive; for example, `totalnum`, `Totalnum`, and `TotalNum` are interpreted as separate variable names.
* There is no official limit on the length of a variable name, but it must fit on one line. Frankly, if your variable names are longer than that—or even longer than 25 or so characters, you might consider a different naming convention.

Three variable declarations:

- var
- let
- const

*/

